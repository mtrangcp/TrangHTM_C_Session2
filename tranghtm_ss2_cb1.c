#include <stdio.h>

int main() {
    // 1. Kieu int (so nguyen)
    int age = 25; 
    
    // 2. Kieu float (so thuc, do chinh xac don)
    float height = 175.5f;
    
    // 3. Kieu double (so thuc, do chinh xac kép)
    double distance = 12345.6789;
    
    // 4. Kieu char (ky tu)
    char grade = 'A';
    
    // 5. Kieu _Bool (kieu boolean, chi co gia tri 0 hoac 1)
    bool isPassed = true; 
    
    // 7. Kieu short (so nguyen ngan)
    short year = 2023; 
    
    // 8. Kieu long (so nguyen dai)
    long largeNumber = 1234567890L;
   
    return 0;
}
